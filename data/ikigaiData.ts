export function calculateIkigaiResults(answersArray: {question: number, answer: string, text: string, pillar: string}[]) {
  const careersScored = CAREERS_DB.map(career => {
    let loveScoreData = 0, goodAtScoreData = 0, paysWellScoreData = 0, worldNeedsScoreData = 0;

    for (const ans of answersArray) {
      const qVal = ans.answer;
      const qNum = ans.question;

      // Type cast as any since data contains multiple types (nested objects or arrays or single values if we used them, but here it's nested dictionary)
      loveScoreData += (career.rules.love as any)[qNum]?.[qVal] || 0;
      goodAtScoreData += (career.rules.goodAt as any)[qNum]?.[qVal] || 0;
      paysWellScoreData += (career.rules.paysWell as any)[qNum]?.[qVal] || 0;
      worldNeedsScoreData += (career.rules.worldNeeds as any)[qNum]?.[qVal] || 0;
    }
    
    const totalScore = loveScoreData + goodAtScoreData + paysWellScoreData + worldNeedsScoreData;
    let rawPercent = Math.round((totalScore / 40) * 100);
    const matchPercent = Math.max(45, rawPercent);

    return {
      name: career.name,
      matchPercent,
      totalScore,
      love: loveScoreData,
      goodAt: goodAtScoreData,
      paysWell: paysWellScoreData,
      worldNeeds: worldNeedsScoreData,
      careerObj: career
    };
  });

  careersScored.sort((a, b) => b.matchPercent - a.matchPercent || b.totalScore - a.totalScore);
  
  const topCareers = careersScored.slice(0, 3).map(c => c.name);
  const bestMatch = careersScored[0];
  const topCareerObj = bestMatch.careerObj;
  
  const getMaxForPillar = (pillarObj: any) => {
    let max = 0;
    for (const q in pillarObj) {
        let maxForQ = 0;
        for (const ans in pillarObj[q]) {
            if (pillarObj[q][ans] > maxForQ) maxForQ = pillarObj[q][ans];
        }
        max += maxForQ;
    }
    return max;
  };

  const maxLove = getMaxForPillar(topCareerObj.rules.love);
  const maxGoodAt = getMaxForPillar(topCareerObj.rules.goodAt);
  const maxPaysWell = getMaxForPillar(topCareerObj.rules.paysWell);
  const maxWorldNeeds = getMaxForPillar(topCareerObj.rules.worldNeeds);

  const ikigaiScores = {
    love: Math.min(100, Math.round((bestMatch.love / maxLove) * 100)),
    goodAt: Math.min(100, Math.round((bestMatch.goodAt / maxGoodAt) * 100)),
    paysWell: Math.min(100, Math.round((bestMatch.paysWell / maxPaysWell) * 100)),
    worldNeeds: Math.min(100, Math.round((bestMatch.worldNeeds / maxWorldNeeds) * 100))
  };

  return {
    ikigaiScores,
    topCareers,
    matchDetails: careersScored
  };
}
